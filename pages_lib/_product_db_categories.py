def map_naver_category(whole_cat_name: str) -> str:
    """네이버 wholeCategoryName → 내부 카테고리명 변환.

    1순위: 2단계 경로명 정확 매핑 (식품>가루/분말류>오트밀 → 2단계="가루/분말류")
    2순위: 전체 경로 키워드 폴백
    """
    if not whole_cat_name:
        return ''
    parts = [p.strip() for p in whole_cat_name.split('>')]

    # 1순위: 2단계 카테고리 정확 매핑
    if len(parts) >= 2:
        l2 = parts[1]
        if l2 in _NAVER_L2_MAP:
            return _NAVER_L2_MAP[l2]
        # 부분 포함 매칭 (예: "과자·간식류" ↔ "과자/간식류")
        for key, cat in _NAVER_L2_MAP.items():
            if key in l2 or l2 in key:
                return cat

    # 2순위: 전체 경로 키워드 폴백
    lower = whole_cat_name.lower()
    for keyword, cat in _NAVER_KEYWORD_MAP:
        if keyword.lower() in lower:
            return cat
    return ''


def guess_category_from_name(product_name: str) -> str:
    """상품명 키워드로 카테고리 추정 (기존 상품 일괄 분류용)."""
    if not product_name:
        return ''
    lower = product_name.lower()
    for keyword, cat in _NAME_CAT_MAP:
        if keyword.lower() in lower:
            return cat
    return ''
